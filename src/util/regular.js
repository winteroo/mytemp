// 存放各中正则表达式，方便查询
const regularObj = {
  // 手机号
  phone: /^1[3456789]\d{9}$/,
  // 身份证
  IDNum: /(^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$)|(^[1-9]\d{5}\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}$)/,
  // 邮箱
  email: /^\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*$/,
  // 8-16位数字字母组合
  password: /^(?![0-9]+$)(?![a-z]+$)(?![A-Z]+$)(?![\W]+$).{8,16}$/,
  // 2-50位汉字
  loginName: /^[a-zA-Z0-9\u4E00-\u9FA5_/-]{2,50}$/,
  // 正整数
  positiveInteger: /^\+?[1-9][0-9]*$/
};
export default regularObj;
